# potencia
potencia = 2 ** 3
print(potencia)
# 8

# raiz
raiz = 2 ** 0.5
print(f"Raiz 2 = {raiz}")
# Raiz 2 = 1.4142135623730951

# raiz cubica
raiz_cubica = 27 ** (1/3)
print(f"Raiz cubica 27 = {raiz_cubica}")
# Raiz cubica 27 = 3.0

# orden de las operaciones
resultado = 3 + 2 * (2 + 2)
print(resultado)
# 11
